package concrete.goonie.core.axis;

/**
 * Defines possible positions for an axis on a chart.
 */
public enum AxisPosition {
    /** Axis on the left side of the chart */
    LEFT,

    /** Axis on the right side of the chart */
    RIGHT,

    /** Axis at the top of the chart */
    TOP,

    /** Axis at the bottom of the chart */
    BOTTOM
}
